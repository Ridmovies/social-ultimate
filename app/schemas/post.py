from pydantic import BaseModel


class PostBase(BaseModel):
    """
    Базовая схема поста.

    Содержит общие поля, которые используются
    как основа для всех остальных схем Post.

    Используется для:
    - наследования в Create / Response схемах
    - устранения дублирования полей
    """
    content: str


class PostCreate(PostBase):
    """
    Схема создания поста.

    Используется при создании нового поста через API.

    Наследует PostBase и не добавляет дополнительных полей,
    так как клиент передаёт только данные для создания.
    """
    pass


class PostPut(PostCreate):
    """
    Схема полного обновления поста (PUT).

    Используется при полном перезаписывании ресурса.

    Ожидается, что клиент передаёт полное состояние объекта.
    """
    pass


class PostPatch(BaseModel):
    """
    Схема частичного обновления поста (PATCH).

    Используется для изменения отдельных полей поста.

    Все поля являются опциональными, так как обновляется
    только часть ресурса.
    """
    content: str | None = None


class PostResponse(PostBase):
    """
    Схема ответа API для поста.

    Используется при возврате данных клиенту.

    Содержит:
    - id (идентификатор поста)
    - content (содержимое поста)

    Представляет финальное состояние объекта,
    возвращаемое из базы данных.
    """
    id: int